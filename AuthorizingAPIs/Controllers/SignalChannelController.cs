        [HttpPost("{id}")]
        [HttpGet("{id}")]
        [Route("/api/signal/getimage/{id}")]
        [AllowAnonymous]
        public async Task<IActionResult> GetImage(Guid id)
        {
            var image = await _signalChannelService.AddSignalImage(id);
            if (image == null)
            {
                return NotFound();
            }

            return File(image, "image/png");
        }


